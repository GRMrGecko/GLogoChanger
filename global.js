function handleMessage(theEvent) {
	if (theEvent.name=="canLoad") {
		var message = theEvent.message;
		if (message.name=="getLogos") {
			theEvent.message = MGMLogoURLs;
		} else if (message.name == "getLogosText") {
			theEvent.message = MGMLogosText;
		} else if (message.name == "getSetting") {
			var data = message.data;
			theEvent.message = safari.extension.settings[data];
		}
	}
}
safari.application.addEventListener("message", handleMessage, false);